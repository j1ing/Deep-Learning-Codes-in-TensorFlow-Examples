# create a training dataframe with filenames and labels
df = pd.DataFrame(trainlist,columns=["image_id"])
"""
            image_id
0       dog.8011.jpg
1       cat.5077.jpg
2       dog.7322.jpg
"""

df["label"] = 0

"""
           image_id  label
0       dog.8011.jpg      0
1       cat.5077.jpg      0
2       dog.7322.jpg      0
"""

from tqdm import tqdm
for i in tqdm(range(len(df))):
    label = df["image_id"][i].split(".")[0]
    df["label"][i] = label

"""
           image_id label
0       dog.8011.jpg   dog
1       cat.5077.jpg   cat
2       dog.7322.jpg   dog
3       cat.2718.jpg   cat
"""

# split the data into training set and validation set
from sklearn.model_selection import train_test_split
train, test = train_test_split(df,test_size=0.2,random_state=101)


from tensorflow.keras.preprocessing.image import ImageDataGenerator
imgen = ImageDataGenerator(rescale=1/255,
                           horizontal_flip=True,
                           vertical_flip=True,
                           zoom_range=0.2,
                           rotation_range=0.2)

train_data = imgen.flow_from_dataframe(dataframe=train,
                                       directory=filepath,
                                       x_col="image_id",
                                       y_col="label",
                                       target_size=(128,128),
                                       class_mode="binary",
                                       seed=101,
                                       batch_size=16)

validation_data = imgen.flow_from_dataframe(dataframe=test,
                                            directory=filepath,
                                            x_col="image_id",
                                            y_col="label",
                                            target_size=(128,128),
                                            class_mode="binary",
                                            seed=101,
                                            batch_size=16)

# class indices
train_data.class_indices
"""
{'cat': 0, 'dog': 1}
"""

